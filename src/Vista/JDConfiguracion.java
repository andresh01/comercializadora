    private void codigo() {
        ConfigC obj = new ConfigC();
        try {
            if (tipo.equals("Categoria")) {
                txtCodigo.setText(String.valueOf(obj.obtenerCodigoCat()));
            } else if (tipo.equals("Tipo Documento")) {
                txtCodigo.setText(String.valueOf(obj.obtenerCodigoTipo()));
            } else if (tipo.equals("Cargos")) {
                obj.obtenerCodigoCargo();
                txtCodigo.setText(String.valueOf(obj.getCodigo()));
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.toString());
        }
    }

