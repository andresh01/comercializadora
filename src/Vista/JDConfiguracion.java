    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        btnGuardar.setEnabled(false);
        btnModificar.setEnabled(true);
        try {
            ConfigC obj = new ConfigC();
            CachedRowSet crs = null;

            if (tipo.equals("Categoria")) {
                crs = obj.buscarCategria(txtNombre.getText());
            } else if (tipo.equals("Tipo Documento")) {
                crs = obj.buscarTipo(txtNombre.getText());
            } else if (tipo.equals("Cargos")) {
                crs = obj.buscarCargo(txtNombre.getText());
            }

            if (crs.next()) {
                txtCodigo.setText(String.valueOf(crs.getInt(1)));
                txtNombre.setText(crs.getString(2));
                txtDescripcion.setText(crs.getString(3));
            } else {
                JOptionPane.showMessageDialog(this, "No se Encontr√≥");
                txtNombre.requestFocus(true);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.toString());
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        String nom = txtNombre.getText();
        String desc = txtDescripcion.getText();
        ConfigC obj = new ConfigC(nom, desc);
        try {
            if (tipo.equals("Categoria")) {

                JOptionPane.showMessageDialog(this, obj.modificarCategotria(Integer.parseInt(txtCodigo.getText())));

            } else {
                if (tipo.equals("Tipo Documento")) {

                    JOptionPane.showMessageDialog(this, obj.modificarTdocumento(Integer.parseInt(txtCodigo.getText())));

                } else {
                    JOptionPane.showMessageDialog(this, obj.modificarCargo(Integer.parseInt(txtCodigo.getText())));
                }
            }
            limpiarPanel();
            codigo();
            btnModificar.setEnabled(false);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.toString());
        }
    }//GEN-LAST:event_btnModificarActionPerformed

