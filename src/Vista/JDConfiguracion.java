    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String nom = txtNombre.getText();
        String desc = txtDescripcion.getText();
        ConfigC obj = new ConfigC(nom, desc);
        try {
            if (tipo.equals("Categoria")) {

                JOptionPane.showMessageDialog(this, obj.guardarCategotria());

            } else {
                if (tipo.equals("Tipo Documento")) {

                    JOptionPane.showMessageDialog(this, obj.guardarTdocumento());

                } else {
                    JOptionPane.showMessageDialog(this, obj.guardarCargo());
                }
            }
            limpiarPanel();
            codigo();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.toString());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

