    private void jTablePMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTablePMouseClicked
        int fila = jTableP.getSelectedRow();
        txtCodigo.setText(dtm.getValueAt(fila, 0).toString());
        txtNombre.setText(dtm.getValueAt(fila, 1).toString());
        txtPrecio.setText(dtm.getValueAt(fila, 2).toString());
        txtStock.setText(dtm.getValueAt(fila, 3).toString());
        cbTipoProducto.setSelectedItem(dtm.getValueAt(fila, 4));
        //jdcVencimiento.setDateFormatString("yyyy-MM-dd");
        jdcVencimiento.setDate(Date.valueOf(dtm.getValueAt(fila, 5).toString()));
        spDescuento.setValue(dtm.getValueAt(fila, 6));
        ckbEstado.setSelected(Boolean.parseBoolean(dtm.getValueAt(fila, 7).toString()));
        habilitarDetalle(true);

    }//GEN-LAST:event_jTablePMouseClicked

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if ((txtCodigo.getText()).equals("")) {
            JOptionPane.showMessageDialog(this, "Elija primero un producto");
        } else {
            try {
                ProductoC obj = new ProductoC();
                String msj = obj.eliminarProducto(Integer.parseInt(txtCodigo.getText()));
                JOptionPane.showMessageDialog(this, msj);
                dtm.setNumRows(0);
                btnMostrar.doClick();
                limpiarDetalle();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.toString());
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

