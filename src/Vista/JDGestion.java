    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        try {
            int cod = Integer.parseInt(txtCodigo.getText());
            String tipoProducto = cbTipoProducto.getSelectedItem().toString();
            String nombre = txtNombre.getText();
            int precio = Integer.parseInt(txtPrecio.getText());
            int stock = Integer.parseInt(txtStock.getText());
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy/MM/dd");
            String fVencimiento = sdf.format(jdcVencimiento.getDate());
            float pDescuento = Float.parseFloat(spDescuento.getValue().toString());
            boolean estado = ckbEstado.isSelected();
            ProductoC obj = new ProductoC(cod, tipoProducto, nombre, precio, stock, fVencimiento, pDescuento, estado);
            JOptionPane.showMessageDialog(this, obj.modificarProducto());
            limpiarDetalle();
            habilitarDetalle(false);
            btnMostrar.doClick();
            
        } catch (Exception ex) {
            Logger.getLogger(JDGestion.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

