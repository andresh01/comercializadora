    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        dtm.setNumRows(0);
        try {
            CachedRowSet crs = new CachedRowSetImpl();
            ProductoC objP = new ProductoC();
            crs = objP.mostrarProductos();

            while (crs.next()) {
                v[0] = crs.getInt(1); //"IdProducto"
                v[1] = crs.getString(3); //"Nombre"
                v[2] = crs.getInt(4); //"ValUnitario"
                v[3] = crs.getInt(5); //"Stock"
                v[4] = crs.getString(2);
                v[5] = crs.getString(6);
                v[6] = crs.getFloat(7);
                v[7] = crs.getBoolean(8);
                dtm.addRow(v);
            }
            jTableP.setModel(dtm);
            ocultarColumnas();

        } catch (Exception ex) {
            Logger.getLogger(JDGestion.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnMostrarActionPerformed

